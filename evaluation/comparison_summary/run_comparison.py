import argparse
import sys
import json
from pathlib import Path

# Add project root to sys.path
sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

from comparison_summary.utils import (
    utc_timestamp, ensure_dir, write_json, write_csv, zip_directory, copy_artifact
)
from comparison_summary.analysis import (
    normalize_baseline_data, build_overall_comparison,
    build_per_category_comparison, build_per_baseline_details
)
from comparison_summary.plots import generate_all_plots
from comparison_summary.report import generate_pdf_report

def parse_args():
    parser = argparse.ArgumentParser(description="Generate a comparison summary across benchmark baselines")
    parser.add_argument("--comparison-name", type=str, default="baseline_comparison", help="Name for the comparison")
    parser.add_argument("--baseline", action="append", help="Baseline info in format: label,results_path[,metrics_path[,token_accounting_path]]")
    parser.add_argument("--output-root", type=str, default="results/comparison_summary", help="Output root directory")
    parser.add_argument("--notes", type=str, help="Optional notes for the comparison")
    return parser.parse_args()

def parse_baseline_arg(arg):
    parts = arg.split(',')
    info = {
        'label': parts[0],
        'results_path': parts[1],
        'metrics_path': parts[2] if len(parts) > 2 else None,
        'token_accounting_path': parts[3] if len(parts) > 3 else None,
    }
    return info

def df_to_markdown(df):
    if df.empty:
        return ""
    cols = df.columns.tolist()
    header = "| " + " | ".join(cols) + " |"
    separator = "| " + " | ".join(["---"] * len(cols)) + " |"
    rows = []
    for _, row in df.iterrows():
        rows.append("| " + " | ".join(str(val) for val in row.values) + " |")
    return "\n".join([header, separator] + rows)

def visible_overall_df(overall_df):
    hidden_columns = {"total_pipeline_tokens_amortized"}
    ordered_columns = [
        "baseline",
        "BLEU",
        "F1",
        "LLM Judge",
        "search_time",
        "response_time",
        "context_tokens",
        "prompt_tokens_total",
        "total_tokens",
        "token_metric_status",
    ]
    columns = [column for column in ordered_columns if column in overall_df.columns and column not in hidden_columns]
    return overall_df[columns].copy()

def write_report_markdown(path, overall_df, cat_df, timestamp, comparison_name, notes):
    report_overall_df = visible_overall_df(overall_df)
    lines = [
        f"# Benchmark Comparison Report: {comparison_name}",
        "",
        f"- **Generated at**: `{timestamp}`",
    ]
    if notes:
        lines.append(f"- **Notes**: {notes}")
    
    lines.extend([
        "",
        "## 📊 Overall Comparison",
        "",
        df_to_markdown(report_overall_df.round(4)),
        "",
        "## 📈 Visual Analysis",
        "",
        "### Quality Comparison",
        "![Quality Comparison](figures/quality_comparison.png)",
        "",
        "### Efficiency (Latency)",
        "![Efficiency Comparison](figures/efficiency_comparison.png)",
        "",
        "### Token Usage",
        "![Token Comparison](figures/token_comparison.png)",
        "",
        "### Quality vs. Cost Trade-off",
        "![Quality vs Tokens](figures/quality_vs_tokens.png)",
        "",
        "### Quality vs. Latency Trade-off",
        "![Quality vs Latency](figures/quality_vs_latency.png)",
        "",
        "## 🗂️ Per Category Detailed Metrics",
        "",
        df_to_markdown(cat_df.round(4)) if not cat_df.empty else "No category data available.",
        "",
        "## 📝 Methodology & Notes",
        "",
        "- **Canonical Tokens**: `total_tokens` is shown as the primary token metric.",
        "- **Token Status**: `canonical_run` means run-level accounting; `canonical_question_mean` means mean question-level accounting when run-level total is not observable.",
        "- **Legacy Tokens**: amortized legacy fields are retained in CSV artifacts only and are not shown in the main report table.",
        "- **Normalization**: Results are averaged across all questions in the dataset.",
        "",
        "---",
        "Generated by Comparison Summary Tool"
    ])
    
    Path(path).write_text("\n".join(lines) + "\n", encoding="utf-8")

def main():
    args = parse_args()
    if not args.baseline:
        print("Error: At least one baseline must be provided.")
        sys.exit(1)
        
    timestamp = utc_timestamp()
    artifact_dir = ensure_dir(Path(args.output_root) / f"{args.comparison_name}_{timestamp}")
    tables_dir = ensure_dir(artifact_dir / "tables")
    figures_dir = ensure_dir(artifact_dir / "figures")
    
    baselines_info = [parse_baseline_arg(b) for b in args.baseline]
    normalized_data = []
    
    print(f"Normalizing data for {len(baselines_info)} baselines...")
    for info in baselines_info:
        print(f"  Processing {info['label']}...")
        data = normalize_baseline_data(info)
        if data:
            normalized_data.append(data)
        else:
            print(f"  Warning: Could not read results for {info['label']} at {info['results_path']}")
            
    if not normalized_data:
        print("Error: No data successfully loaded.")
        sys.exit(1)
        
    # Build tables
    print("Building comparative tables...")
    overall_df = build_overall_comparison(normalized_data)
    cat_df = build_per_category_comparison(normalized_data)
    details_df = build_per_baseline_details(normalized_data)
    
    # Save tables
    overall_df.to_csv(tables_dir / "overall_comparison.csv", index=False)
    cat_df.to_csv(tables_dir / "per_category_comparison.csv", index=False)
    details_df.to_csv(tables_dir / "per_baseline_details.csv", index=False)
    
    # Generate plots
    print("Generating comparative plots...")
    generate_all_plots(overall_df, figures_dir)
    
    # Generate Report Markdown
    print("Generating report.md...")
    write_report_markdown(artifact_dir / "report.md", overall_df, cat_df, timestamp, args.comparison_name, args.notes)
    
    # Generate PDF report (keeping it as legacy/alternative)
    print("Generating comparison_report.pdf...")
    try:
        generate_pdf_report(
            overall_df, cat_df, details_df, 
            artifact_dir / "comparison_report.pdf", 
            figures_dir, timestamp, args.comparison_name
        )
    except Exception as e:
        print(f"  Warning: PDF generation failed: {e}")
    
    # Generate manifest
    manifest = {
        "comparison_name": args.comparison_name,
        "timestamp": timestamp,
        "baselines_included": [info['label'] for info in baselines_info],
        "overall_summary": overall_df.to_dict(orient='records'),
        "notes": args.notes,
        "artifacts": {
            "report_md": "report.md",
            "report_pdf": "comparison_report.pdf",
            "tables": "tables/",
            "figures": "figures/",
            "manifest": "comparison_manifest.json"
        }
    }
    write_json(artifact_dir / "comparison_manifest.json", manifest)
    
    # Zip artifacts
    zip_path = Path(args.output_root) / f"{args.comparison_name}_{timestamp}.zip"
    print(f"Zipping artifacts to {zip_path}...")
    zip_directory(artifact_dir, zip_path)
    
    print(f"Done! Artifacts saved in {artifact_dir}")

if __name__ == "__main__":
    main()
